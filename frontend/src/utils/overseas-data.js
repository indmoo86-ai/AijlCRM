/**
 * 获取大洲名称
 */
export function getContinentLabel(value) {
  const continent = overseasData.find(c => c.value === value)
  return continent ? continent.label : value
}

/**
 * 获取国家名称
 */
export function getCountryLabel(continentValue, countryValue) {
  const continent = overseasData.find(c => c.value === continentValue)
  if (continent) {
    const country = continent.children.find(c => c.value === countryValue)
    return country ? country.label : countryValue
  }
  return countryValue
}
